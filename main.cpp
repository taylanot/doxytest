/**
 * @brief Adds two numbers.
 *
 * @param a First number
 * @param b Second number
 * @return Sum of a and b
 */
int add(int a, int b)
{
  return a + b;
}

/**
 * @brief Entry point.
 */
int main()
{
  int result = add(2, 3);
  return 0;
}
